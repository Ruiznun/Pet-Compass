class Animal  {
    constructor(id_animal, name, size, breed, age, picture, description, shelter_id) {
        this.id_animal = id_animal;
        this.name = name;
        this.size = size;
        this.breed = breed;
        this.age = age;
        this.picture = picture;
        this.description = description;
        this.shelter_id = shelter_id;
    }

}